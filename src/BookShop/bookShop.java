        public void sales(){
        try {
            String tcost = txtTcost.getText();
            String pay = txtPay.getText();
            String bal = txtBalance.getText();
            
            int lastId = 0;
            
            String query1 = "insert into sales(SubTotal,Pay,Balance) values(?,?,?)";
            pst = (PreparedStatement) con.prepareStatement(query1,Statement.RETURN_GENERATED_KEYS);
            pst.setString(1, tcost);
            pst.setString(2, pay);
            pst.setString(3, bal);
            pst.executeUpdate();
            rst = pst.getGeneratedKeys();
            
            if(rst.next())
                lastId = rst.getInt(1);
            
            String query2 = "insert into sales_product(Sales_Id, Book_Name, Price, Qty, Total) values(?, ?, ?, ?, ?)";
            pst1 = (PreparedStatement) con.prepareStatement(query2);
            String bname;
            String price;
            String qty;
            int total;
            
            for(int i=0;i<addBookTable.getRowCount();i++){
                bname = (String)addBookTable.getValueAt(i, 0);
                price = (String)addBookTable.getValueAt(i, 1);
                qty = (String)addBookTable.getValueAt(i, 2);
                total = (int)addBookTable.getValueAt(i, 3);
                pst1.setInt(1, lastId);
                pst1.setString(2, bname);
                pst1.setString(3, price);
                pst1.setString(4, qty);
                pst1.setInt(5, total);
                pst1.executeUpdate();
            }
            JOptionPane.showMessageDialog(this, "Sales Completed");
                txtPay.setText("");
                txtBalance.setText("");
                txtTcost.setText("");
                txtBid.requestFocus();
                
            HashMap map = new HashMap();
            map.put("invo",lastId);
            
            try {
                JasperDesign design = JRXmlLoader.load("C:\\Users\\Prakhar\\Documents\\NetBeansProjects\\BookInvnt\\src\\BookShop\\report1.jrxml");
                JasperReport report = JasperCompileManager.compileReport(design);
                JasperPrint print = JasperFillManager.fillReport(report, map, con);
                JasperViewer.viewReport(print);
            } catch (Exception e) {
            }
        } catch (SQLException ex) {
            Logger.getLogger(bookShop.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        }
