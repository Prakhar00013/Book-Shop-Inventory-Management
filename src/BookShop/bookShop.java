    private void txtBidKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtBidKeyPressed
        // TODO add your handling code here:
        if(evt.getKeyCode() == KeyEvent.VK_ENTER){
            try {
                String bId = txtBid.getText();
                pst = (PreparedStatement) con.prepareStatement("Select * from book where ID = ?");
                pst.setString(1, bId);
                rst = pst.executeQuery();
                if(rst.next() == false)
                    JOptionPane.showMessageDialog(this, "Book ID not found");
                else{
                    String bName = rst.getString("BookName");
                    txtBname.setText(bName);
                    String bprice = rst.getString("Price");
                    txtPrice.setText(bprice);
                    txtQty.requestFocus();
                }
            } catch (SQLException ex) {
                Logger.getLogger(bookShop.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_txtBidKeyPressed

